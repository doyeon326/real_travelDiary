package com.travel.vo;

public class AttendeesVO {
	
	private int idx;
	private int board_idx;
	private int attendees_idx;
	private String name;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getIdx() {
		return idx;
	}
	public void setIdx(int idx) {
		this.idx = idx;
	}
	public int getBoard_idx() {
		return board_idx;
	}
	public void setBoard_idx(int board_idx) {
		this.board_idx = board_idx;
	}
	public int getAttendees_idx() {
		return attendees_idx;
	}
	public void setAttendees_idx(int attendees_idx) {
		this.attendees_idx = attendees_idx;
	}
	
	

}
